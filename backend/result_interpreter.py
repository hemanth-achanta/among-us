"""
LLM-powered result interpretation.

``ResultInterpreter`` takes a pandas DataFrame (the raw query result) and asks
the LLM to produce an analyst-grade answer to the original question.

The interpretation is designed to:
* Ground the answer in actual query output with specific numbers.
* Perform RCA-style reasoning when appropriate.
* Identify patterns, outliers, and key contributors.
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import Any

import pandas as pd
import numpy as np

from llm.llm_client import LLMClient
from llm.prompt_templates import (
    build_interpretation_messages,
    build_multi_interpretation_messages,
)
from utils.logger import Timer, get_logger

log = get_logger(__name__)

# Max characters of the results table to include in the interpretation prompt.
# Sized to fit full query results (queries are already limited to 500 rows).
_MAX_TABLE_CHARS = 150_000


# ─────────────────────────────────────────────────────────────────────────────
# Data classes
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class InterpretationResult:
    """Human-readable answer generated by the LLM."""

    answer:          str
    model_used:      str
    duration_ms:     float
    prompt_system:   str
    prompt_messages: list[dict[str, str]]
    raw_llm_content: str = ""
    stop_reason:     str = "unknown"
    output_truncated: bool = False
    input_tokens:    int = 0
    output_tokens:   int = 0
    max_tokens_budget: int = 0
    table_data_chars_original: int = 0
    table_data_chars_sent:     int = 0
    table_data_was_truncated:  bool = False
    stats_chars_sent:          int = 0


# ─────────────────────────────────────────────────────────────────────────────
# Interpreter
# ─────────────────────────────────────────────────────────────────────────────

class ResultInterpreter:
    """
    Generates a stakeholder-friendly explanation from SQL query results.

    Parameters
    ----------
    llm_client: Shared :class:`~llm.llm_client.LLMClient` instance.
    """

    def __init__(self, llm_client: LLMClient) -> None:
        self._llm = llm_client

    # ── Public API ────────────────────────────────────────────────────────────

    def interpret(
        self,
        question: str,
        sql: str,
        dataframe: pd.DataFrame,
        model: str,
        chat_history: list[dict[str, str]] | None = None,
        business_context: str = "",
        result_truncated: bool = False,
        result_max_rows: int | None = None,
    ) -> InterpretationResult:
        """
        Ask the LLM to explain the query results with analyst-grade depth.

        Computes lightweight summary statistics for numeric columns and
        passes them alongside the table data so the LLM has both raw rows
        and aggregate context for richer interpretation.
        """
        row_count = len(dataframe)
        results_table, table_meta = self._format_table_with_meta(dataframe)
        result_stats = self._compute_stats(dataframe)

        result_truncated_at_rows = (
            result_max_rows if (result_truncated and result_max_rows is not None) else None
        )

        messages, system = build_interpretation_messages(
            question=question,
            sql=sql,
            results_table=results_table,
            row_count=row_count,
            chat_history=chat_history,
            result_stats=result_stats,
            business_context=business_context,
            result_truncated_at_rows=result_truncated_at_rows,
        )

        log.info(
            "interpretation_start",
            model=model,
            row_count=row_count,
            table_chars_original=table_meta["original_chars"],
            table_chars_sent=table_meta["sent_chars"],
            table_truncated=table_meta["was_truncated"],
        )

        with Timer() as t:
            response = self._llm.complete(
                model=model,
                messages=messages,
                system=system,
            )

        result = InterpretationResult(
            answer=response.content.strip(),
            model_used=response.model,
            duration_ms=t.elapsed_ms,
            prompt_system=system,
            prompt_messages=messages,
            raw_llm_content=response.content,
            stop_reason=response.stop_reason,
            output_truncated=response.output_truncated,
            input_tokens=response.input_tokens,
            output_tokens=response.output_tokens,
            max_tokens_budget=response.max_tokens_budget,
            table_data_chars_original=table_meta["original_chars"],
            table_data_chars_sent=table_meta["sent_chars"],
            table_data_was_truncated=table_meta["was_truncated"],
            stats_chars_sent=len(result_stats),
        )

        log.info(
            "interpretation_complete",
            model=model,
            answer_length=len(result.answer),
            stop_reason=response.stop_reason,
            output_truncated=response.output_truncated,
            duration_ms=round(t.elapsed_ms, 1),
        )

        return result

    def interpret_no_results(
        self,
        question: str,
        sql: str,
        model: str,
        chat_history: list[dict[str, str]] | None = None,
    ) -> InterpretationResult:
        """
        Generate a helpful interpretation when the query returned zero rows.

        Explains possible reasons and suggests adjustments.
        """
        answer = (
            "The query executed successfully but **returned no results**. "
            "This could mean:\n"
            "- The date range or filters are too restrictive — try broadening them.\n"
            "- The data for that specific combination doesn't exist in the table.\n"
            "- The partition date (`dt`) filter may not cover the requested period.\n\n"
            "Could you try rephrasing with a broader date range or different filters?"
        )
        log.info("interpretation_empty_result", question=question)
        return InterpretationResult(
            answer=answer,
            model_used=model,
            duration_ms=0.0,
            prompt_system="",
            prompt_messages=[],
        )

    # ── Helpers ───────────────────────────────────────────────────────────────

    @staticmethod
    def _format_table(df: pd.DataFrame) -> str:
        """
        Convert a DataFrame to a readable string, prioritising information
        density over raw row count to stay within token budget.
        """
        text, _ = ResultInterpreter._format_table_with_meta(df)
        return text

    @staticmethod
    def _format_table_with_meta(df: pd.DataFrame) -> tuple[str, dict]:
        """
        Like _format_table but also returns metadata about truncation.

        Returns (formatted_text, {"original_chars", "sent_chars", "was_truncated"}).
        """
        if df.empty:
            return "(empty)", {"original_chars": 0, "sent_chars": 7, "was_truncated": False}

        try:
            show_rows = min(len(df), 60)
            text = df.head(show_rows).to_string(index=False)
        except Exception:  # noqa: BLE001
            cols = ", ".join(df.columns.tolist())
            text = f"Columns: {cols}\n({len(df)} rows)"

        original_chars = len(text)
        was_truncated = original_chars > _MAX_TABLE_CHARS

        if was_truncated:
            text = text[:_MAX_TABLE_CHARS] + "\n... [table truncated for context]"

        meta = {
            "original_chars": original_chars,
            "sent_chars": len(text),
            "was_truncated": was_truncated,
        }
        return text, meta

    @staticmethod
    def _compute_stats(df: pd.DataFrame) -> str:
        """
        Compute lightweight summary statistics for numeric columns.

        Returns a compact string with sum/mean/min/max for each numeric
        column, giving the LLM aggregate context alongside the raw rows.
        This is token-efficient: ~50 chars per column vs. thousands for rows.
        """
        if df.empty or len(df) < 2:
            return ""

        numeric_cols = df.select_dtypes(include=[np.number]).columns.tolist()
        if not numeric_cols:
            return ""

        lines = ["## Column statistics (numeric columns)"]
        for col in numeric_cols[:15]:
            try:
                series = df[col].dropna()
                if series.empty:
                    continue
                total = series.sum()
                mean = series.mean()
                mn = series.min()
                mx = series.max()
                lines.append(
                    f"  {col}: sum={total:,.2f}, mean={mean:,.2f}, "
                    f"min={mn:,.2f}, max={mx:,.2f}"
                )
            except Exception:  # noqa: BLE001
                continue

        return "\n".join(lines) if len(lines) > 1 else ""

    def interpret_multi(
        self,
        question: str,
        sql_list: list[str],
        dataframes: list[pd.DataFrame],
        model: str,
        labels: list[str] | None = None,
        chat_history: list[dict[str, str]] | None = None,
        business_context: str = "",
        truncated_flags: list[bool] | None = None,
        result_max_rows: int | None = None,
    ) -> InterpretationResult:
        """
        Interpret multiple result sets that together answer the user's question.

        Parameters
        ----------
        question:   Original user question.
        sql_list:   List of SQL strings that were executed.
        dataframes: List of DataFrames corresponding to each SQL string.
        model:      Anthropic model ID to use for interpretation.
        labels:     Optional human-friendly labels for each query (for context).
        """
        if not sql_list or not dataframes or len(sql_list) != len(dataframes):
            # Fallback to a generic message if inputs are inconsistent.
            answer = (
                "I was unable to interpret the results because of an internal "
                "pipeline inconsistency."
            )
            log.error("interpretation_multi_invalid_inputs")
            return InterpretationResult(
                answer=answer,
                model_used=model,
                duration_ms=0.0,
                prompt_system="",
                prompt_messages=[],
            )

        queries_payload: list[dict[str, Any]] = []
        for idx, (sql, df) in enumerate(zip(sql_list, dataframes), start=1):
            label = labels[idx - 1] if labels and idx - 1 < len(labels) else f"Query {idx}"
            results_table = self._format_table(df)
            row_count = len(df)
            truncated = (
                truncated_flags[idx - 1]
                if truncated_flags and idx - 1 < len(truncated_flags)
                else False
            )
            payload: dict[str, Any] = {
                "label": label,
                "sql": sql,
                "results_table": results_table,
                "row_count": str(row_count),
            }
            if truncated and result_max_rows is not None:
                payload["truncated_at_rows"] = result_max_rows
            queries_payload.append(payload)

        messages, system = build_multi_interpretation_messages(
            question=question,
            queries=queries_payload,
            chat_history=chat_history,
            business_context=business_context,
        )

        log.info(
            "interpretation_multi_start",
            model=model,
            query_count=len(sql_list),
        )

        with Timer() as t:
            response = self._llm.complete(
                model=model,
                messages=messages,
                system=system,
            )

        result = InterpretationResult(
            answer=response.content.strip(),
            model_used=response.model,
            duration_ms=t.elapsed_ms,
            prompt_system=system,
            prompt_messages=messages,
            raw_llm_content=response.content,
            stop_reason=response.stop_reason,
            output_truncated=response.output_truncated,
            input_tokens=response.input_tokens,
            output_tokens=response.output_tokens,
            max_tokens_budget=response.max_tokens_budget,
        )

        log.info(
            "interpretation_multi_complete",
            model=model,
            answer_length=len(result.answer),
            stop_reason=response.stop_reason,
            output_truncated=response.output_truncated,
            duration_ms=round(t.elapsed_ms, 1),
            query_count=len(sql_list),
        )

        return result
